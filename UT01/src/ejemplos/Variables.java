package ejemplos;


public class Variables {


	public static void main(String[] args) {

		/*
		 * mas muestras de ejemplo con concatenacion
		 * y variables
		 */


		int a=18;

		System.out.println(a);
		System.out.println("a");
		System.out.println(a+1);
		System.out.println("a"+1);

		/*
		 * ahora vemos variables con la propia operacion hecha
		 * en la declaracion
		 */

		a=18+1;
		System.out.println(a);

		a=18-1;
		System.out.println(a);


		a=a*2;
		System.out.println(a);


		a=a/2;
		System.out.println(a);




	}

}
