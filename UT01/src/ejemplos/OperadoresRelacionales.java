package ejemplos;

public class OperadoresRelacionales {

	public static void main(String[] args) {
		
		//Declaro e inicializo variables
		int a=9, b=2;
		
		//Repasamos los operadores relacionales
		System.out.println(a+"<"+b+"\t"+(a<b));
		System.out.println(a+">"+b+"\t"+(a>b));
		System.out.println(a+"<="+b+"\t"+(a<=b));
		System.out.println(a+">="+b+"\t"+(a>=b));
		System.out.println(a+"=="+b+"\t"+(a==b));
		System.out.println(a+"!="+b+"\t"+(a!=b));

	}

}
