package ejemplos;

public class MasOperaciones {

	public static void main(String[] args) {
		
		int x =3, y=8;
		
		System.out.println(" x="+ x + " y="+ y);
		x++;
		y--;
		System.out.println(" x="+ x + " y="+ y);
		x+=y;
		System.out.println(" x="+ x + " y="+ y);
		x-=3;
		y*=2;
		System.out.println(" x="+ x + " y="+ y);
	}

}
