package ejemplos;

public class Operaciones {

	public static void main(String[] args) {
		//Numeros enteros
		int a=5;
		
		System.out.println("El valor de a es: "+a);
		System.out.println("El doble de a es: "+2*a);
		System.out.println("El triple de a es: "+3*a);
		System.out.println("La mitad de a es: "+a/2);
		
		System.out.println("\n"); //imprimos salto de linea
		
		//Numeros reales
		float b=5;
		
		System.out.println("El valor de b es: "+b);
		System.out.println("El doble de b es: "+2*b);
		System.out.println("El triple de b es: "+3*b);
		System.out.println("La mitad de b es: "+b/2);
	
		
		

	}

}
