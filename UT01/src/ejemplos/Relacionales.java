package ejemplos;

public class Relacionales {

	public static void main(String[] args) {
		
		int a=6;
		int b=7;
		
		//Probamos los distintos operadores relacionales
		
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a<=b);
		System.out.println(a>=b);
		System.out.println(a==b);
		System.out.println(a!=b);
		

	}

}
