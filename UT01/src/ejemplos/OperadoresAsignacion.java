package ejemplos;

public class OperadoresAsignacion {

	public static void main(String[] args) {
		
		//Declaracion e inicializacion de las variables
		int a =2, b=3;
		
		//Practicamos los operadores de asignacion
		a=b;
		System.out.println(a+" "+b);
		a=a+b;
		System.out.println(a+" "+b);
		a=a+1;
		System.out.println(a+" "+b);
		b=b-1;
		System.out.println(a+" "+b);

	}

}
